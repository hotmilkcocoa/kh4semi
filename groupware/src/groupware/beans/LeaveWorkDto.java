package groupware.beans;

public class LeaveWorkDto {
	private int leave_no;
	private int emp_no;
	private int vac_no;
	
	public LeaveWorkDto() {
		super();
	}

	public int getLeave_no() {
		return leave_no;
	}

	public void setLeave_no(int leave_no) {
		this.leave_no = leave_no;
	}

	public int getEmp_no() {
		return emp_no;
	}

	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}

	public int getVac_no() {
		return vac_no;
	}

	public void setVac_no(int vac_no) {
		this.vac_no = vac_no;
	}
	
	
}
